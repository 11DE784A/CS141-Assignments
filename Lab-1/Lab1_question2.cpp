#include<iostream>
using namespace std;
// Write a C++ program to enter two numbers and find their sum.

int main(){
	int firstNumber, secondNumber;

	cout << "This program is supposed to find the sum of two numbers." << endl;

	cout << "Enter the first number: ";
	cin >> firstNumber;

	cout << "Enter the second number: ";
	cin >> secondNumber;

	cout << endl << "The sum of the two entered numbers is: " << firstNumber + secondNumber << "." << endl << endl;

	return 0;
}
